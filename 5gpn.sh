  mkdir -p "${DNSDIST_DIR}"
  touch "${DNSDIST_DIR}/direct-local.txt" "${DNSDIST_DIR}/proxy-local.txt" "${DNSDIST_DIR}/block-local.txt"

  # Direct: explicit exclusions and iOS/Apple network probes that must not be proxied.
  cat > "${DNSDIST_DIR}/direct-local.txt.default" <<'EOF'
