#!/usr/bin/env bash
set -euo pipefail

# 5GPN optimized installer
# - Bootstraps Xiuyixx/5GPN
# - Applies the current proven policy:
#   direct-local -> proxy-local -> ChinaList -> default proxy
# - Keeps iOS profile/QR output
# - Updates remote AI rules daily
# - Disables UDP/443 QUIC path and suppresses proxy-domain AAAA/HTTPS/SVCB

BASE_DIR="/opt/proxy-gateway"
DNSDIST_DIR="/etc/dnsdist"
WWW_DIR="${BASE_DIR}/www"
IOS_PROFILE_PORT="${IOS_PROFILE_PORT:-8111}"
UPSTREAM_INSTALL_URL="${UPSTREAM_INSTALL_URL:-https://raw.githubusercontent.com/Xiuyixx/5GPN/main/install.sh}"
OPENAI_RULE_URL="${OPENAI_RULE_URL:-https://raw.githubusercontent.com/EAlyce/conf/main/Rule/OpenAI.list}"

DEFAULT_PRIVATE_OVERSEAS_DNS="${PRIVATE_OVERSEAS_DNS:-1.1.1.1 8.8.8.8 9.9.9.9}"
DEFAULT_PUBLIC_OVERSEAS_DNS="${PUBLIC_OVERSEAS_DNS:-1.1.1.1 8.8.8.8}"
DEFAULT_SNIPROXY_DNS="${SNIPROXY_DNS:-1.1.1.1 8.8.8.8 9.9.9.9}"

log() { printf '\033[0;34m[INFO]\033[0m %s\n' "$*"; }
ok() { printf '\033[0;32m[OK]\033[0m   %s\n' "$*"; }
warn() { printf '\033[1;33m[WARN]\033[0m %s\n' "$*" >&2; }
err() { printf '\033[0;31m[ERR]\033[0m  %s\n' "$*" >&2; }

need_root() {
  if [[ "${EUID}" -ne 0 ]]; then
    err "Run as root."
    exit 1
  fi
}

prompt_if_empty() {
  local var_name="$1" prompt="$2" default="${3:-}" value
  value="${!var_name:-}"
  if [[ -z "${value}" && -t 0 ]]; then
    if [[ -n "${default}" ]]; then
      read -r -p "${prompt} [${default}]: " value
      value="${value:-$default}"
    else
      read -r -p "${prompt}: " value
    fi
    printf -v "${var_name}" '%s' "${value}"
  fi
}

public_ip() {
  curl -4fsS --max-time 8 https://api.ipify.org 2>/dev/null ||
    curl -4fsS --max-time 8 https://ifconfig.me 2>/dev/null ||
    ip route get 1.1.1.1 2>/dev/null | sed -n 's/.* src \([0-9.]*\).*/\1/p' | head -n1
}

install_upstream_if_needed() {
  if [[ -x /usr/local/bin/update-dnsdist-rules.sh && -f "${DNSDIST_DIR}/dnsdist.conf.template" ]]; then
    log "Existing 5GPN installation detected; skipping upstream installer."
    return 0
  fi

  prompt_if_empty DOMAIN "Domain for DoT certificate (must already resolve to this server)"
  if [[ -z "${DOMAIN:-}" ]]; then
    err "DOMAIN is required for non-interactive optimized install."
    exit 1
  fi
  prompt_if_empty EMAIL "Let's Encrypt email" "admin@${DOMAIN}"

  log "Running upstream 5GPN installer. Domain: ${DOMAIN}"
  export DOMAIN EMAIL
  export PRIVATE_OVERSEAS_DNS="${PRIVATE_OVERSEAS_DNS:-$DEFAULT_PRIVATE_OVERSEAS_DNS}"
  export PUBLIC_OVERSEAS_DNS="${PUBLIC_OVERSEAS_DNS:-$DEFAULT_PUBLIC_OVERSEAS_DNS}"
  export SNIPROXY_DNS="${SNIPROXY_DNS:-$DEFAULT_SNIPROXY_DNS}"

  bash -c "$(curl -fsSL "${UPSTREAM_INSTALL_URL}")"
}

ensure_swap() {
  if swapon --show | grep -q .; then
    ok "Swap already enabled."
    return 0
  fi
  log "Enabling 1G swapfile."
  if command -v fallocate >/dev/null 2>&1; then
    fallocate -l 1G /swapfile || dd if=/dev/zero of=/swapfile bs=1M count=1024
  else
    dd if=/dev/zero of=/swapfile bs=1M count=1024
  fi
  chmod 600 /swapfile
  mkswap /swapfile
  swapon /swapfile
  grep -q '^/swapfile ' /etc/fstab || echo '/swapfile none swap sw 0 0' >> /etc/fstab
}

