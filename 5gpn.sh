write_healthcheck() {
  cat > /usr/local/bin/5gpn-healthcheck.sh <<'EOF'
#!/usr/bin/env bash
set -euo pipefail
SERVER_IP=$(cat /etc/dnsdist/.public_ip 2>/dev/null || ip route get 1.1.1.1 2>/dev/null | grep -oP 'src \K[\d.]+' || echo 127.0.0.1)
DOT_NAME=$(cat /etc/dnsdist/.domain 2>/dev/null || echo localhost)
fail=0

check_service() {
  local svc="$1"
  if systemctl is-active --quiet "$svc"; then echo "OK service $svc"; else echo "FAIL service $svc"; fail=1; fi
}

check_dot() {
  python3 - "$SERVER_IP" "$DOT_NAME" <<'PY' || return 1
import socket, ssl, struct, random, sys
server, sni = sys.argv[1], sys.argv[2]
checks = [("chatgpt.com",1,server),("auth.openai.com",1,server),("chatgpt.com",65,None),("auth.openai.com",65,None),("baidu.com",1,"not-proxy"),("captive.apple.com",1,"not-proxy")]
def enc(name):
    out=b""
    for p in name.split("."): out += bytes([len(p)]) + p.encode()
    return out+b"\0"
def skip(data, off):
    while True:
        l=data[off]
        if l & 0xc0: return off+2
        if l == 0: return off+1
        off += 1+l
def query(name, qtype):
    pkt=struct.pack("!HHHHHH", random.randrange(65536),0x0100,1,0,0,0)+enc(name)+struct.pack("!HH",qtype,1)
    ctx=ssl._create_unverified_context(); raw=socket.create_connection((server,853),timeout=5); s=ctx.wrap_socket(raw,server_hostname=sni)
    s.sendall(struct.pack("!H",len(pkt))+pkt); hdr=s.recv(2); n=struct.unpack("!H",hdr)[0]
    data=b""
    while len(data)<n: data += s.recv(n-len(data))
    s.close(); an=struct.unpack("!H",data[6:8])[0]; off=12
    for _ in range(struct.unpack("!H",data[4:6])[0]): off=skip(data,off)+4
    ips=[]
    for _ in range(an):
        off=skip(data,off); typ,cls,ttl,rdlen=struct.unpack("!HHIH",data[off:off+10]); off+=10
        rdata=data[off:off+rdlen]; off+=rdlen
        if typ==1 and rdlen==4: ips.append(socket.inet_ntoa(rdata))
    return an, ips
bad=False
for name,qtype,expect in checks:
    an,ips=query(name,qtype)
    ok = (an == 0) if expect is None else ((server not in ips and an > 0) if expect == "not-proxy" else expect in ips)
    print(("OK" if ok else "FAIL"), "dot", name, qtype, ips, "answers", an)
    bad = bad or not ok
raise SystemExit(1 if bad else 0)
PY
}

check_tcp_sni() {
  if timeout 8 openssl s_client -connect "${SERVER_IP}:443" -servername chatgpt.com -brief </dev/null 2>&1 | grep -q 'Peer certificate: CN=chatgpt.com'; then
    echo "OK tcp443 chatgpt.com"
  else
    echo "FAIL tcp443 chatgpt.com"
    fail=1
  fi
}

check_service dnsdist
check_service sniproxy
check_dot || fail=1
check_tcp_sni
free -h | sed 's/^/MEM /'
exit "$fail"
EOF
  chmod +x /usr/local/bin/5gpn-healthcheck.sh
  cat > /etc/systemd/system/5gpn-healthcheck.service <<'EOF'
[Unit]
Description=5GPN healthcheck

[Service]
Type=oneshot
ExecStart=/usr/local/bin/5gpn-healthcheck.sh
EOF
  cat > /etc/systemd/system/5gpn-healthcheck.timer <<'EOF'
[Unit]
Description=Run 5GPN healthcheck every 5 minutes

[Timer]
OnBootSec=2min
OnUnitActiveSec=5min
Persistent=true

[Install]
WantedBy=timers.target
EOF
  systemctl daemon-reload
  systemctl enable --now 5gpn-healthcheck.timer
}

